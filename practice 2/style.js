
// 1--how we can access html elements using dom in javascript?

// 1.getElementById: This method retrieves an element based on its unique id attribute.

const elements=document.getElementById('id')

// 2.getElementsByClassName: This method returns a collection of elements that have a specific class name.

const elements=document.getElementsByClassName('classname')

// 3.getElementsByTagName: This method returns a collection of elements with the specified tag name

const elements = document.getElementsByTagName('tagName');

// 4.querySelector: This method returns the first element that matches a specified CSS selector.

const element = document.querySelector('selector');

// 5.querySelectorAll: This method returns a collection of elements that match a specified CSS selector.

const elements = document.querySelectorAll('selector');

// 6.getElementsByName: This method returns a collection of elements with a specific name attribute.

const elements = document.getElementsByName('elementName');

